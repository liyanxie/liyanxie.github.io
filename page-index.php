<h2>Research</h2>
<p>Advisor: Professor <a href="https://www2.isye.gatech.edu/~yxie77/" target=&ldquo;blank&rdquo;>Yao Xie</a></p>
<p>My research interest lies in statistics and optimization, with a primary focus on sequential change-point detection and their applications in sensor networks and industrial quality control. </p>
<p>I am very fortunate to collaborate with Professors <a href="http://www.ssp.ece.upatras.gr/moustakides/" target=&ldquo;blank&rdquo;>George V. Moustakides</a>, Professor <a href="https://faculty.mccombs.utexas.edu/rui.gao/" target=&ldquo;blank&rdquo;>Rui Gao</a>, Professor <a href="https://noise.earth.utah.edu" target=&ldquo;blank&rdquo;>Fan-Chi Lin</a>, and Professor <a href="http://sensorweb.engr.uga.edu/index.php/song/" target=&ldquo;blank&rdquo;>WenZhan Song</a>.</p>
